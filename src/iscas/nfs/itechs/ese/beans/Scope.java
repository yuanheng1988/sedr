package iscas.nfs.itechs.ese.beans;

public class Scope {
	protected String scope = "";
	protected String scopeScale = "";
	
	public String getScope() {
		return scope;
	}
	
	public void setScope(String scope) {
		this.scope = scope;
	}
	
	public String getScopeScale() {
		return scopeScale;
	}
	
	public void setScopeScale(String scopeScale) {
		this.scopeScale = scopeScale;
	}
	
	public String toString() {
		StringBuffer sb = new StringBuffer();
		
		return sb.toString();
	}
}
